"""
Day 2: 變數與基本資料型態
"""

# int
age = 34
print(f"name: {age}")
print(type(age))

# string
name = "Allen"
print(f"name: {name}")
print(type(name))

# float
money = 3.15
print(f"money: {money}")
print(type(money))

# boolean
is_transaction = False
print(f"is_transaction: {is_transaction}")
print(type(is_transaction))

# list
friuts = ["apple", "banana", "cherry"]
print(f"friuts: {friuts}")
print(type(friuts))

# tuple
colors = ("red", "green","blue")
print(f"colors: {colors}")
print(type(colors))

# dictionary
person = { "name": "Abby", "age": 8, "sex": "female"}
print(f"person: {person}")
print(type(person))

# set
unique_numbers={1,2,2,3,3,3,4,4,5}
print(f"unique_numbers: {unique_numbers}")
print(type(unique_numbers))

# NoneType
data = None
print(f"data: {data}")
print(type(data))